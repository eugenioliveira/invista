<?php

namespace App\Providers;

use App\Contracts\AddressApi;
use App\Services\ViaCepApi;
use Illuminate\Support\Facades\Schema;
use Illuminate\Support\ServiceProvider;

class AppServiceProvider extends ServiceProvider
{
    /**
     * Register any application services.
     *
     * @return void
     */
    public function register()
    {
        // Configura a API do ViaCep como padrÃ£o
        $this->app->bind(AddressApi::class, ViaCepApi::class);
    }

    /**
     * Bootstrap any application services.
     *
     * @return void
     */
    public function boot()
    {
        // Laravel uses the utf8mb4 character set by default, which includes support for storing "emojis" in the database.
        // if you are running a version of MySQL older than the 5.7.7 release or MariaDB older than the 10.2.2 release,
        // you may need to manually configure the default string length generated by migrations in order
        // for MySQL to create indexes for them.
        Schema::defaultStringLength(191);
    }
}
